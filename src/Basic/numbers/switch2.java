package Basic.numbers;

public class switch2
{
    static void main() {
        int day=3;
        switch (day)
        {
            case 0:
                System.out.println("Monday");
                break;
            case 1:
                System.out.println("Tuesday");
                break;
            case 2:
                System.out.println("Wednesday");
                break;
            case 3:
                System.out.println("Thrusday");
                break;
            default:
                System.out.println("Not found");
        }
    }
}
