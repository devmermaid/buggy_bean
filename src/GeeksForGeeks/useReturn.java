package GeeksForGeeks;

public class useReturn
{
    //Given an integer n, You need to use the return keyword to return twice the value of n.

        //Examples:

    //Input: n = 2
    //Output: 4
    //Explanation: 2 * 2 = 4

    static int display(int n)
    {
        return 2*n;
    }

    static void main() {
        System.out.print("Output is: ");
        System.out.println(useReturn.display(2));;
    }

}
